//! psql.rs — every PSQL module type driven from Rust.
//!
//! The rsfbclient twin of ../cpp/psql.cpp: the document's worked examples —
//! an executable procedure (hire) with INSERT ... RETURNING into an output
//! parameter, a BEFORE INSERT trigger writing an audit row, a selectable
//! procedure (raises) streaming SUSPENDed rows, and a custom PSQL exception.
//! Where the C++ sample reads the output message of EXECUTE PROCEDURE by
//! hand, rsfbclient's execute_returnable() maps the single output row to a
//! tuple; the selectable procedure is simply SELECTed from; and the custom
//! exception surfaces as an FbError carrying the same status vector the C++
//! sample formats — including the "At procedure ... line: N, col: M" PSQL
//! stack trace.  See ../../psql-and-stored-procedures.md.
//!
//! Run (see ../README.md):  cargo run --bin psql

use fb_handson_rust::connect;
use rsfbclient::{prelude::*, FbError, SimpleTransaction};

fn main() -> Result<(), FbError> {
    let mut conn = connect("psql")?;

    // -- Schema: the document's worked examples, made idempotent. ---------
    // On re-runs the procedures still reference emp; reduce them to stubs
    // first so RECREATE TABLE is not blocked by dependencies.
    let mut ddl = SimpleTransaction::new(&mut conn, TransactionConfiguration::default())?;
    ddl.execute(
        "CREATE OR ALTER PROCEDURE hire (p_name VARCHAR(30), p_salary NUMERIC(10,2)) \
         RETURNS (new_id INTEGER) AS BEGIN END",
        (),
    )?;
    ddl.execute(
        "CREATE OR ALTER PROCEDURE raises (pct NUMERIC(5,2)) \
         RETURNS (id INTEGER, name VARCHAR(30), new_salary NUMERIC(10,2)) \
         AS BEGIN END",
        (),
    )?;
    ddl.commit_retaining()?;
    ddl.execute(
        "RECREATE TABLE emp (id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, \
         name VARCHAR(30), salary NUMERIC(10,2))",
        (),
    )?;
    ddl.execute(
        "RECREATE TABLE audit_log (logged_at TIMESTAMP, what VARCHAR(60))",
        (),
    )?;
    ddl.execute("CREATE OR ALTER EXCEPTION low_salary 'salary below minimum'", ())?;
    ddl.commit_retaining()?;

    ddl.execute(
        "CREATE OR ALTER PROCEDURE hire (p_name VARCHAR(30), p_salary NUMERIC(10,2))\n\
         RETURNS (new_id INTEGER) AS\n\
         BEGIN\n\
         \x20 IF (p_salary < 1000) THEN EXCEPTION low_salary;\n\
         \x20 INSERT INTO emp (name, salary) VALUES (:p_name, :p_salary)\n\
         \x20   RETURNING id INTO :new_id;\n\
         END",
        (),
    )?;
    ddl.execute(
        "CREATE OR ALTER TRIGGER emp_bi BEFORE INSERT ON emp AS\n\
         BEGIN\n\
         \x20 INSERT INTO audit_log VALUES (CURRENT_TIMESTAMP,\n\
         \x20   'insert emp ' || COALESCE(NEW.name, '?'));\n\
         END",
        (),
    )?;
    ddl.execute(
        "CREATE OR ALTER PROCEDURE raises (pct NUMERIC(5,2))\n\
         RETURNS (id INTEGER, name VARCHAR(30), new_salary NUMERIC(10,2)) AS\n\
         BEGIN\n\
         \x20 FOR SELECT id, name, salary FROM emp INTO :id, :name, :new_salary DO\n\
         \x20 BEGIN\n\
         \x20   new_salary = new_salary * (1 + pct / 100);\n\
         \x20   SUSPEND;\n\
         \x20 END\n\
         END",
        (),
    )?;
    ddl.commit()?;

    let mut tr = SimpleTransaction::new(&mut conn, TransactionConfiguration::default())?;

    // -- 1. Executable procedure: one output message, no cursor. ----------
    for call in [
        "EXECUTE PROCEDURE hire('Ada', 5000)",
        "EXECUTE PROCEDURE hire('Grace', 6000)",
    ] {
        let (new_id,): (i64,) = tr.execute_returnable(call, ())?;
        println!("{:<42} -> NEW_ID = {}", call, new_id);
    }

    // -- 2. The trigger fired once per insert. -----------------------------
    let (audits,): (i64,) = tr
        .query_first("SELECT COUNT(*) FROM audit_log", ())?
        .unwrap();
    println!("audit_log rows (trigger emp_bi):              {}", audits);

    // -- 3. Selectable procedure: SUSPEND streams rows into a cursor. ------
    println!("\nSELECT * FROM raises(10):");
    let rows: Vec<(i64, String, f64)> = tr.query(
        "SELECT id, name, new_salary FROM raises(10) ORDER BY id",
        (),
    )?;
    println!("    {:<4} {:<8} {:>10}", "ID", "NAME", "NEW_SALARY");
    for (id, name, new_salary) in rows {
        println!("    {:<4} {:<8} {:>10.2}", id, name, new_salary);
    }

    // -- 4. The custom exception, with its PSQL stack trace. ---------------
    println!("\nEXECUTE PROCEDURE hire('Poorpay', 500) ->");
    match tr.execute_returnable::<(), (i64,)>("EXECUTE PROCEDURE hire('Poorpay', 500)", ()) {
        Ok(_) => println!("BUG: the low_salary exception did not fire"),
        Err(e) => println!("{}", e),
    }

    tr.commit()?;
    println!("done.");
    Ok(())
}
